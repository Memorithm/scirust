//! **Route B — checkpoint quality evaluation on the CUDA backend**
//! (feature `eval` = `cuda` + `syn`). Steps 2–4 of the post-first-run plan: put a
//! number on how good a trained checkpoint actually is, before spending more compute
//! scaling it up. Loads a checkpoint (+ optional BPE tokenizer), reports the exact
//! loss/perplexity/nats-per-char picture, generates a **deterministic batch** of
//! samples (seed varied per sample), and scores them:
//!
//! - **metrics** — token-level loss (train, from the checkpoint meta, vs. a freshly
//!   measured held-out val loss when `SCIAGENT_SHARDS`/`SCIAGENT_TEXT` is given),
//!   perplexity `exp(loss)`, and nats/char (`val_loss / chars_per_token`).
//! - **UTF-8 rate** — fraction of samples whose raw generated bytes are valid UTF-8
//!   (only < 100% for byte-level models; a BPE decode is UTF-8 by construction).
//! - **balanced rate** — fraction with correctly-nested `()`/`[]`/`{}` (lexical).
//! - **syn-parse rate** — fraction that `syn::parse_file` accepts as valid Rust.
//! - **rustc-compile rate** — optional (`SCIAGENT_RUSTC=1`): fraction that compile as
//!   a `--crate-type lib` (needs `rustc` on `PATH`; writes temp files).
//! - **repetition / diversity** — mean trigram-repeat rate, longest single-token run,
//!   and type-token ratio (distinct / total) over the generated tokens.
//!
//! Env (shares the `cuda_generate` knobs):
//! - `SCIAGENT_CKPT` — a checkpoint dir (`step_N/`, or a parent holding `step_*`).
//! - `SCIAGENT_TOKENIZER` — BPE tokenizer json (required for a non-byte vocab).
//! - `SCIAGENT_SHARDS` / `SCIAGENT_TEXT` — held-out corpus for the exact val loss +
//!   nats/char (tail `SCIAGENT_VAL_FRAC`, default 2%). Omit to report train-only.
//! - `SCIAGENT_SAMPLES` (32), `SCIAGENT_MAX_NEW` (256), `SCIAGENT_SEED` (0 base),
//!   `SCIAGENT_PROMPT` (`fn `), `SCIAGENT_TEMP` (0.8), `SCIAGENT_TOP_K` (0),
//!   `SCIAGENT_TOP_P` (0.95), `SCIAGENT_REP_PENALTY` (1.1), `SCIAGENT_REP_WINDOW` (64).
//! - `SCIAGENT_VAL_FRAC` (0.02), `SCIAGENT_EVAL_SEQ` (256), `SCIAGENT_EVAL_WINDOWS` (64).
//! - `SCIAGENT_RUSTC` (0) — set to 1 to also run the `rustc` compile check.
//! - `SCIAGENT_SHOW` (2) — how many full samples to print verbatim.
//!
//! ```text
//! SCIAGENT_CKPT=checkpoints/bpe350m SCIAGENT_TOKENIZER=tokenizer.json \
//!   SCIAGENT_SHARDS=$HOME/data/shards SCIAGENT_SAMPLES=32 \
//!   cargo run -p scirust-sciagent --features eval --release --example cuda_eval
//! ```
//!
//! Exit code 2 means no CUDA device — run on the Jetson Thor.

use std::path::Path;
use std::process::{Command, Stdio};

use scirust_sciagent::bpe::BpeTokenizer;
use scirust_sciagent::cuda_model::CudaModel;
use scirust_sciagent::generate::SamplingParams;
use scirust_sciagent::model::SciAgentModel;
use scirust_sciagent::train::checkpoint::{latest_checkpoint, load_checkpoint, read_meta};
use scirust_sciagent::train::dataset::ShardLoader;

fn env_usize(key: &str, default: usize) -> usize {
    std::env::var(key)
        .ok()
        .and_then(|v| v.parse().ok())
        .unwrap_or(default)
}

fn env_f32(key: &str, default: f32) -> f32 {
    std::env::var(key)
        .ok()
        .and_then(|v| v.parse().ok())
        .unwrap_or(default)
}

fn env_flag(key: &str) -> bool {
    std::env::var(key)
        .map(|v| matches!(v.as_str(), "1" | "true" | "yes" | "on"))
        .unwrap_or(false)
}

/// Correctly-nested `()`/`[]`/`{}`? A purely lexical check (it does not skip string
/// or comment contents), so it is a structural proxy, not a guarantee — but a model
/// that can't even balance its brackets is a clear negative signal.
fn brackets_balanced(s: &str) -> bool {
    let mut stack = Vec::new();
    for c in s.chars()
    {
        match c
        {
            '(' | '[' | '{' => stack.push(c),
            ')' | ']' | '}' =>
            {
                let want = match c
                {
                    ')' => '(',
                    ']' => '[',
                    _ => '{',
                };
                if stack.pop() != Some(want)
                {
                    return false;
                }
            },
            _ =>
            {},
        }
    }
    stack.is_empty()
}

/// Fraction of overlapping token trigrams that are repeats (1 − distinct/total).
/// 0 = every trigram unique, → 1 = the model is looping. A blunt but honest
/// degeneracy signal on the *generated* continuation only.
fn trigram_repeat_rate(tokens: &[u32]) -> f32 {
    if tokens.len() < 3
    {
        return 0.0;
    }
    let mut seen = std::collections::HashSet::new();
    let total = tokens.len() - 2;
    let mut distinct = 0usize;
    for w in tokens.windows(3)
    {
        if seen.insert((w[0], w[1], w[2]))
        {
            distinct += 1;
        }
    }
    1.0 - distinct as f32 / total as f32
}

/// Longest run of the *same* token repeated consecutively (a stuck decoder).
fn max_token_run(tokens: &[u32]) -> usize {
    let mut best = 0usize;
    let mut cur = 0usize;
    let mut prev: Option<u32> = None;
    for &t in tokens
    {
        if Some(t) == prev
        {
            cur += 1;
        }
        else
        {
            cur = 1;
            prev = Some(t);
        }
        best = best.max(cur);
    }
    best
}

/// Type-token ratio: distinct / total tokens (higher = more diverse).
fn type_token_ratio(tokens: &[u32]) -> f32 {
    if tokens.is_empty()
    {
        return 0.0;
    }
    let mut set = std::collections::HashSet::new();
    for &t in tokens
    {
        set.insert(t);
    }
    set.len() as f32 / tokens.len() as f32
}

/// Compile `src` as a `--crate-type lib` with `rustc` (edition 2021), emitting only
/// metadata to a temp path so nothing is linked. Returns `Some(ok)` if `rustc` ran,
/// `None` if it couldn't be invoked at all (not on `PATH`).
fn rustc_compiles(src: &str, idx: usize) -> Option<bool> {
    let dir = std::env::temp_dir();
    let src_path = dir.join(format!("scirust_eval_{idx}.rs"));
    let out_path = dir.join(format!("scirust_eval_{idx}.meta"));
    if std::fs::write(&src_path, src).is_err()
    {
        return None;
    }
    let status = Command::new("rustc")
        .args(["--edition", "2021", "--crate-type", "lib", "--emit=metadata"])
        .arg("-o")
        .arg(&out_path)
        .arg(&src_path)
        .stdout(Stdio::null())
        .stderr(Stdio::null())
        .status();
    let _ = std::fs::remove_file(&src_path);
    let _ = std::fs::remove_file(&out_path);
    match status
    {
        Ok(s) => Some(s.success()),
        Err(_) => None,
    }
}

/// Tail `frac` of `tokens` as the held-out split (matches the pretrain val split).
fn tail_split(tokens: &[u32], frac: f32) -> &[u32] {
    let n = tokens.len();
    let val = ((n as f32) * frac).round() as usize;
    let val = val.clamp(0, n);
    &tokens[n - val..]
}

fn main() {
    let ckpt = match std::env::var("SCIAGENT_CKPT")
    {
        Ok(c) => c,
        Err(_) =>
        {
            eprintln!("set SCIAGENT_CKPT to a checkpoint dir (e.g. checkpoints/bpe350m)");
            std::process::exit(1);
        },
    };
    let dir = latest_checkpoint(Path::new(&ckpt)).unwrap_or_else(|| ckpt.clone().into());
    let meta = read_meta(&dir).unwrap_or_else(|e| panic!("read meta {}: {e}", dir.display()));
    let mut model = SciAgentModel::new(&meta.config);
    load_checkpoint(&mut model, &dir).unwrap_or_else(|e| panic!("load {}: {e}", dir.display()));
    let vocab = meta.config.vocab_size;
    println!(
        "checkpoint {} — step {}, train loss {:.4} | vocab {vocab}, d {}, {} layers, {:.1}M params",
        dir.display(),
        meta.step,
        meta.loss,
        meta.config.d_model,
        meta.config.n_layers,
        meta.config.total_parameters() as f64 / 1e6,
    );

    // Optional BPE tokenizer (required for a non-byte vocab).
    let tokenizer = std::env::var("SCIAGENT_TOKENIZER")
        .ok()
        .map(|p| BpeTokenizer::load_json(&p).unwrap_or_else(|e| panic!("load tokenizer {p}: {e}")));
    if tokenizer.is_none() && vocab > 256
    {
        eprintln!(
            "model vocab is {vocab} (BPE) but no SCIAGENT_TOKENIZER set — cannot encode the prompt\n\
             or decode/score output. Point SCIAGENT_TOKENIZER at the tokenizer json."
        );
        std::process::exit(1);
    }

    let Some(cm) = CudaModel::from_model(&model)
    else
    {
        eprintln!("no CUDA device available. Run on the Jetson Thor.");
        std::process::exit(2);
    };

    // ---- Step 2: exact metrics ---------------------------------------------------
    let train_loss = meta.loss;
    println!("\n=== metrics ===");
    println!(
        "train loss (checkpoint meta):  {train_loss:.4} nats/token   (perplexity {:.2})",
        train_loss.exp()
    );

    // Held-out val loss + nats/char, if a corpus is supplied.
    let val_frac = env_f32("SCIAGENT_VAL_FRAC", 0.02);
    let eval_seq = env_usize("SCIAGENT_EVAL_SEQ", 256).min(meta.config.max_seq_len);
    let eval_windows = env_usize("SCIAGENT_EVAL_WINDOWS", 64);
    let val_tokens: Option<Vec<u32>> = if let Ok(sd) = std::env::var("SCIAGENT_SHARDS")
    {
        let mut loader = ShardLoader::new();
        match loader.load_dir(&sd)
        {
            Ok(_) => Some(tail_split(loader.tokens(), val_frac).to_vec()),
            Err(e) =>
            {
                eprintln!("could not load shards from {sd}: {e} — skipping val metrics");
                None
            },
        }
    }
    else if let Ok(text) = std::env::var("SCIAGENT_TEXT")
    {
        match std::fs::read(&text)
        {
            Ok(b) => Some(tail_split(&b.iter().map(|&x| x as u32).collect::<Vec<_>>(), val_frac)
                .to_vec()),
            Err(e) =>
            {
                eprintln!("could not read {text}: {e} — skipping val metrics");
                None
            },
        }
    }
    else
    {
        None
    };

    if let Some(val) = &val_tokens
    {
        let val_loss = cm.eval_loss(val, eval_seq, eval_windows);
        // chars/token: decode the whole val stream and count Unicode scalar values.
        // (For a BPE token, decode(&[id]) returns its text — specials/placeholders
        // contribute 0 chars; for a byte model each token is one byte.)
        let total_chars: usize = match &tokenizer
        {
            Some(tok) => val
                .iter()
                .map(|&t| tok.decode(&[t as usize]).chars().count())
                .sum(),
            None =>
            {
                let bytes: Vec<u8> = val.iter().map(|&t| t as u8).collect();
                String::from_utf8_lossy(&bytes).chars().count()
            },
        };
        let chars_per_token = if val.is_empty()
        {
            0.0
        }
        else
        {
            total_chars as f32 / val.len() as f32
        };
        let nats_per_char = if chars_per_token > 0.0 { val_loss / chars_per_token } else { f32::NAN };
        println!(
            "val   loss (held-out {:.0}% tail): {val_loss:.4} nats/token   (perplexity {:.2})",
            val_frac * 100.0,
            val_loss.exp()
        );
        println!(
            "chars/token {chars_per_token:.3}  →  nats/char {nats_per_char:.4}  ({} val tokens, {eval_windows} windows × {eval_seq})",
            val.len()
        );
        let gap = val_loss - train_loss;
        println!(
            "generalization gap (val − train): {gap:+.4} nats/token  ({})",
            if gap.abs() < 0.5
            {
                "healthy — still generalizing"
            }
            else if gap > 0.0
            {
                "val worse — watch for overfitting"
            }
            else
            {
                "val better — undertrained / train-loss is a noisy single-step estimate"
            }
        );
    }
    else
    {
        println!(
            "(no SCIAGENT_SHARDS / SCIAGENT_TEXT — val loss + nats/char skipped; set one to measure\n\
             generalization and nats/char precisely)"
        );
    }

    // ---- Step 3: deterministic sample batch --------------------------------------
    let prompt_str = std::env::var("SCIAGENT_PROMPT").unwrap_or_else(|_| "fn ".into());
    let prompt: Vec<u32> = match &tokenizer
    {
        Some(tok) => tok
            .encode_with_special(&prompt_str, true, false)
            .iter()
            .map(|&i| i as u32)
            .collect(),
        None => prompt_str.bytes().map(u32::from).collect(),
    };
    if prompt.is_empty()
    {
        eprintln!("empty prompt");
        std::process::exit(1);
    }
    if tokenizer.is_some()
    {
        let unk = prompt.iter().filter(|&&t| t == 3).count();
        if unk * 2 > prompt.len()
        {
            eprintln!(
                "tokenizer error: {unk}/{} prompt tokens are <unk> — this tokenizer.json can't encode\n\
                 the text (mismatched or corrupt). Re-run train-tokenizer + collect-data.",
                prompt.len()
            );
            std::process::exit(1);
        }
    }

    let params = SamplingParams {
        temperature: env_f32("SCIAGENT_TEMP", 0.8),
        top_k: env_usize("SCIAGENT_TOP_K", 0),
        top_p: env_f32("SCIAGENT_TOP_P", 0.95),
        repetition_penalty: env_f32("SCIAGENT_REP_PENALTY", 1.1),
        repetition_window: env_usize("SCIAGENT_REP_WINDOW", 64),
    };
    let n_samples = env_usize("SCIAGENT_SAMPLES", 32);
    let max_new = env_usize("SCIAGENT_MAX_NEW", 256)
        .min(meta.config.max_seq_len.saturating_sub(prompt.len()).max(1));
    let base_seed: u64 = std::env::var("SCIAGENT_SEED")
        .ok()
        .and_then(|v| v.parse().ok())
        .unwrap_or(0);
    let run_rustc = env_flag("SCIAGENT_RUSTC");
    let show = env_usize("SCIAGENT_SHOW", 2);

    println!("\n=== generating {n_samples} deterministic samples ===");
    println!(
        "prompt {:?} ({} tok) | max_new {max_new} | temp {} top_k {} top_p {} rep {} | seeds {base_seed}..{}",
        prompt_str,
        prompt.len(),
        params.temperature,
        params.top_k,
        params.top_p,
        params.repetition_penalty,
        base_seed + n_samples as u64
    );

    // Per-sample scoring, aggregated. Each sample is a fresh deterministic generation
    // at seed = base_seed + i (same prompt, varied seed → a reproducible batch).
    let mut utf8_ok = 0usize;
    let mut balanced_ok = 0usize;
    let mut syn_ok = 0usize;
    let mut rustc_ok = 0usize;
    let mut rustc_ran = 0usize;
    let mut sum_trigram = 0.0f32;
    let mut sum_ttr = 0.0f32;
    let mut sum_run = 0usize;
    let mut samples: Vec<String> = Vec::new();

    for i in 0..n_samples
    {
        let seed = base_seed + i as u64;
        let out = cm.generate(&prompt, max_new, &params, seed);
        let generated = &out[prompt.len().min(out.len())..];

        // Raw generated bytes → valid UTF-8? For BPE the decoded String is UTF-8 by
        // construction, so reconstruct bytes from decoding for a byte model, else use
        // the decoded text (always ok). The byte-level path is the meaningful one.
        let (text, utf8_valid): (String, bool) = match &tokenizer
        {
            Some(tok) => (tok.decode(&out.iter().map(|&t| t as usize).collect::<Vec<_>>()), true),
            None =>
            {
                let bytes: Vec<u8> = out.iter().map(|&t| t as u8).collect();
                match String::from_utf8(bytes.clone())
                {
                    Ok(s) => (s, true),
                    Err(_) => (String::from_utf8_lossy(&bytes).into_owned(), false),
                }
            },
        };

        if utf8_valid
        {
            utf8_ok += 1;
        }
        if brackets_balanced(&text)
        {
            balanced_ok += 1;
        }
        if syn::parse_file(&text).is_ok()
        {
            syn_ok += 1;
        }
        if run_rustc
        {
            if let Some(ok) = rustc_compiles(&text, i)
            {
                rustc_ran += 1;
                if ok
                {
                    rustc_ok += 1;
                }
            }
        }
        sum_trigram += trigram_repeat_rate(generated);
        sum_ttr += type_token_ratio(generated);
        sum_run += max_token_run(generated);

        if samples.len() < show
        {
            samples.push(text);
        }
    }

    // ---- Step 4: the report ------------------------------------------------------
    let pct = |k: usize| 100.0 * k as f32 / n_samples as f32;
    println!("\n=== quality over {n_samples} samples ===");
    println!("valid UTF-8:        {:.1}%  ({utf8_ok}/{n_samples})", pct(utf8_ok));
    println!("balanced ()[]{{}}:    {:.1}%  ({balanced_ok}/{n_samples})", pct(balanced_ok));
    println!("syn::parse_file ok: {:.1}%  ({syn_ok}/{n_samples})", pct(syn_ok));
    if run_rustc
    {
        if rustc_ran > 0
        {
            println!(
                "rustc compiles:     {:.1}%  ({rustc_ok}/{rustc_ran} ran)",
                100.0 * rustc_ok as f32 / rustc_ran as f32
            );
        }
        else
        {
            println!("rustc compiles:     n/a  (rustc not found on PATH)");
        }
    }
    println!(
        "trigram repeat:     {:.1}% (mean)   type-token ratio {:.3} (mean)   longest run {:.1} tok (mean)",
        100.0 * sum_trigram / n_samples as f32,
        sum_ttr / n_samples as f32,
        sum_run as f32 / n_samples as f32
    );

    for (i, s) in samples.iter().enumerate()
    {
        println!("\n--- sample {i} (seed {}) ---\n{s}", base_seed + i as u64);
    }
}
