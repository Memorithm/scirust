#!/usr/bin/env bash
# vendor_scirust_rsi.sh
# ---------------------------------------------------------------------------
# Self-contained installer: recreates the `scirust-rsi` crate inside THIS repo
# (no network / no git fetch needed) so it can be used as a local path
# dependency. Run from the root of the Memorithm/RSI repository:
#
#     bash vendor_scirust_rsi.sh
#
# Then add to RSI/Cargo.toml:
#
#     [dependencies]
#     scirust-rsi = { path = "vendor/scirust-rsi" }
#
# and switch the consumer code from `crate::ascent::*` to `scirust_rsi::*`
# (note the spelling: the engine's elitist primitive is `scirust_rsi::ascend`).
# ---------------------------------------------------------------------------
set -euo pipefail

DEST="${1:-vendor/scirust-rsi}"
EXPECT_SHA="3a98970149f6e474c97e48af2c944575cd67200ac8c6cd48a5d8276adbbdcb31"

echo ">> Vendoring scirust-rsi into ${DEST}"
mkdir -p "${DEST}"
TMP="$(mktemp)"
trap 'rm -f "${TMP}"' EXIT

# Decode the embedded crate archive (base64) appended at the end of this file.
sed -n '/^__ARCHIVE_BELOW__$/,$p' "$0" | tail -n +2 | base64 -d > "${TMP}"

if command -v sha256sum >/dev/null 2>&1; then
  GOT="$(sha256sum "${TMP}" | cut -d' ' -f1)"
  if [ "${GOT}" != "${EXPECT_SHA}" ]; then
    echo "!! checksum mismatch: got ${GOT}, expected ${EXPECT_SHA}" >&2
    exit 1
  fi
  echo ">> checksum OK (${GOT})"
fi

tar -xzf "${TMP}" -C "${DEST}"
echo ">> Done. Files written:"
find "${DEST}" -type f | sort | sed 's/^/   /'
cat <<'NEXT'

Next steps
----------
1. Add to RSI/Cargo.toml:
       [dependencies]
       scirust-rsi = { path = "vendor/scirust-rsi" }
2. In the consumer code, replace the local engine with the vendored one:
       use scirust_rsi::{Guard, Fitness, Report, ascend};
       use scirust_rsi::refine::{RefineTask, SelfRefiner};
3. Build & verify offline:
       cargo build && cargo test
       cargo run -p scirust-rsi --example rsi_demo   # smoke test of the engine
NEXT

exit 0

